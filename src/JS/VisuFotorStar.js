document.getElementById("ThirdRowThirdLayout").addEventListener("mouseover", () => {
    document.getElementById("tileFotor").style.left = "-15%";
    document.getElementById("tileStar").style.left = "50%";
})

document.getElementById("ThirdRowThirdLayout").addEventListener("mouseout", () => {
    document.getElementById("tileFotor").style.left = "-25%";
    document.getElementById("tileStar").style.left = "55%";
})

